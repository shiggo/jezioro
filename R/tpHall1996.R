#'   \item Column 2: Genus (of the diatom taxa)
#'   \item Column 3: Species (of the diatom taxa)
#'   \item Column 4-57: Relative abundance data (%) of the diatom taxa in each study lake
#'   
#'   \item Row 1: Total phosphorus (TP) measurements (ug/L) for each lake
#'   \item Rows 2-425: Diatom relative abundance data by taxon
#' }
#' @docType data
#' @usage data(tpHall1996)
#' @format A data frame containing 425 observations of 57 variables
